//
// double isNetscape (void)
//
////////////////////////////////////////////////////////////////////////////////////////////
//
// Return value:
//   Version of Netscape the user is using, if applicable
//
////////////////////////////////////////////////////////////////////////////////////////////
//
// Parameter:
//    Not available
//
// Usage:
//    This functions detects what version of Netscape the viewer is using
//    and returns it.
//
////////////////////////////////////////////////////////////////////////////////////////////

function isNetscape()
{
//  1.0 version of code block
//	if (stristr($GLOBALS["HTTP_USER_AGENT"], "Mozilla/4.7") || stristr($GLOBALS["HTTP_USER_AGENT"], "Gecko")) {

	// Netscape 4.7
	if (stristr(getenv("HTTP_USER_AGENT"), "Mozilla/4.7")) {
		return "4.7";
	} 
	
	// Netscape 6.1
	if (strstr(getenv("HTTP_USER_AGENT"), "Netscape6/6.0") || stristr(getenv("HTTP_USER_AGENT"), "Netscape6/6.1")) {
		return "6.1";
	}
	
	// Netscape 6.2
	if (stristr(getenv("HTTP_USER_AGENT"), "Netscape6/6.2")) {
		return "6.2";
	} else {
		return "6.0";
	}
}

////////////////////////////////////////////////////////////////////////////////////////////
//
// int absNetscape (void)
//
////////////////////////////////////////////////////////////////////////////////////////////
//
// Return value:
//   TRUE if viewer is using *ANY* version of Netscape, otherwise return FALSE
//
////////////////////////////////////////////////////////////////////////////////////////////
//
// Parameter:
//    Not available
//
// Usage:
//    This function detects whether or not the viewer is using any Netscape browser,
//    hence absoluteNetscape().
//
////////////////////////////////////////////////////////////////////////////////////////////

function absNetscape()
{
	if (stristr($GLOBALS["HTTP_USER_AGENT"], "Mozilla/4.7")) {
		return true;
	}
	else
		return false;
}
